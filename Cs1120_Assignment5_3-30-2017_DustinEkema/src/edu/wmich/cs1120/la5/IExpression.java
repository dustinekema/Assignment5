package edu.wmich.cs1120.la5;

/**
 * This is the IExpression interface. It sets the required methods for any class that 
 * implements this interface.
 * 
 * @author Ricardo
 *
 */
public interface IExpression {
	
	/**
	 * This method sets a required method for a class. This will return the value of the expression.
	 * 
	 * @return Integer this is the integer value that is returned.
	 */
	Integer getValue();
}
